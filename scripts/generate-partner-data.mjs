// Generates src/data/partners.ts from public/partners/*.png filenames.
// Run: node scripts/generate-partner-data.mjs
import { readdirSync, writeFileSync } from 'node:fs'

const files = readdirSync('public/partners').filter(f => f.endsWith('.png'))

function nameFromFile(file) {
  return file
    .replace(/^LOGOS-SITE-/i, '')
    .replace(/^banners?-site-?\d*-?/i, '')
    .replace(/-200x120\.png$/i, '')
    .replace(/-v\d+$/i, '')
    .replace(/[-_]+/g, ' ')
    .trim()
    .toLowerCase()
    .replace(/\b\w/g, c => c.toUpperCase())
}

function idFromName(name) {
  return name.toLowerCase().replace(/\s+/g, '-').replace(/[^a-z0-9-]/g, '')
}

const partners = files.map(file => {
  const name = nameFromFile(file)
  return {
    id: idFromName(name) || file.replace(/\.png$/i, '').toLowerCase(),
    name,
    sector: 'outro',
    reach: 3,
    logo: `/partners/${file}`,
  }
})

const out = `// Auto-generated by scripts/generate-partner-data.mjs from public/partners/*.png filenames.
// Refine sector / reach / instagram / related manually as Henrique provides metadata.

export type PartnerSector =
  | 'beleza'
  | 'saude'
  | 'educacao'
  | 'gastronomia'
  | 'servicos'
  | 'moda'
  | 'tech'
  | 'outro'

export interface Partner {
  id: string
  name: string
  instagram?: string
  sector: PartnerSector
  reach: 1 | 2 | 3 | 4 | 5
  logo: string
  related?: string[]
}

export const SHOW_CONSTELLATION_LINES = false

export const partners: Partner[] = ${JSON.stringify(partners, null, 2)}
`

writeFileSync('src/data/partners.ts', out)
console.log(`Wrote ${partners.length} partners to src/data/partners.ts`)
